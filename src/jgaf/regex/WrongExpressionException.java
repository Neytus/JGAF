package jgaf.regex;

/**
 *
 * @author hanis
 */
public class WrongExpressionException extends RuntimeException {

    /**
     * Creates a new instance of <code>WrongExpressionException</code> without detail message.
     */
    public WrongExpressionException() {
    }


    /**
     * Constructs an instance of <code>WrongExpressionException</code> with the specified detail message.
     * @param msg the detail message.
     */
    public WrongExpressionException(String msg) {
        super(msg);
    }

    
}
